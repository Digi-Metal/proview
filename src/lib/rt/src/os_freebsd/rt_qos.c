pwr_tBoolean
qos_WaitQue (
  pwr_tStatus		*status,
  qdb_sQue		*qp,
  int			tmo
)
{
  struct timespec ts;
  int remaining_time = tmo;
  int delta = 100;
	
  ts.tv_sec = 0;

  qdb_AssumeLocked;

  qp->lock.waiting = TRUE;

  qdb_Unlock;

  if ( tmo == -1) {
    ts.tv_nsec = delta * 1000000;

    while (1) {

      if ( !qp->lock.waiting) {
        *status = QCOM__SUCCESS;
        qdb_Lock;
        return 1;
      }
      nanosleep( &ts, 0);
    }
  }
  else {
    while (1) {

      if ( !qp->lock.waiting) {
        *status = QCOM__SUCCESS;
        qdb_Lock;
        return 1;
      }

      if ( !remaining_time) {
        /* Timeout */
        *status = QCOM__TMO;
        qdb_Lock;
        return 0;
      }
      if ( remaining_time <= delta) {
        ts.tv_nsec = remaining_time * 1000000;  
        remaining_time = 0;
      }
      else {
        ts.tv_nsec = delta * 1000000;
        remaining_time -= delta;
      }
      nanosleep( &ts, 0);
    }
  }
  return 0;
}


