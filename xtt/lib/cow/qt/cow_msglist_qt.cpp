/* co_msglist_qt.cpp -- Message list */

#include "cow_msglist_qt.h"
#include "cow_qt_helpers.h"

#include "flow_browwidget_qt.h"

MsgListQt::MsgListQt(void* ev_parent_ctx, QWidget* ev_parent_wid, QWidget** w)
    : MsgList(ev_parent_ctx)
{
  debug_print("creating a scrolledbrowwidgetqt\n");
  form_widget
      = scrolledbrowwidgetqt_new(MsgList::init_brow_cb, this, &brow_widget);

  // The widget is not realized until it has processed a show event. By calling
  // widget->show(), Qt processes the show event at some arbitrary time in the
  // future. But in order to use the widget, we need to realize it NOW!
  // This way, we force Qt to process the show() event immediately.
  showNow(brow_widget);

  // Create the root item
  *w = form_widget;
}

MsgListQt::~MsgListQt()
{
  debug_print("MsgListQt::~MsgListQt\n");
  delete brow;
  form_widget->close();
}

void MsgListQt::set_input_focus()
{
  brow_widget->setFocus();
}